public class livros {
    private int codlivros;
    
    private String Titulo;
    private String Autor;
    private String ISBN;
    private String Editora;
    private String Ano_publicacao;
    private String Genero;
    private String Numero_paginas;
    private String Idioma;
    private String Localizacao_biblioteca;

    public int getCodlivros() {
        return codlivros;
    }

    public void setCodlivros(int codlivros) {
        this.codlivros = codlivros;
    }

    public String getTitulo() {
        return Titulo;
    }

    public void setTitulo(String Titulo) {
        this.Titulo = Titulo;
    }

    public String getAutor() {
        return Autor;
    }

    public void setAutor(String Autor) {
        this.Autor = Autor;
    }

    public String getISBN() {
        return ISBN;
    }

    public void setISBN(String ISBN) {
        this.ISBN = ISBN;
    }

    public String getEditora() {
        return Editora;
    }

    public void setEditora(String Editora) {
        this.Editora = Editora;
    }

    public String getAno_publicacao() {
        return Ano_publicacao;
    }

    public void setAno_publicacao(String Ano_publicacao) {
        this.Ano_publicacao = Ano_publicacao;
    }

    public String getGenero() {
        return Genero;
    }

    public void setGenero(String Genero) {
        this.Genero = Genero;
    }

    public String getNumero_paginas() {
        return Numero_paginas;
    }

    public void setNumero_paginas(String Numero_paginas) {
        this.Numero_paginas = Numero_paginas;
    }

    public String getIdioma() {
        return Idioma;
    }

    public void setIdioma(String Idioma) {
        this.Idioma = Idioma;
    }

    public String getLocalizacao_biblioteca() {
        return Localizacao_biblioteca;
    }

    public void setLocalizacao_biblioteca(String Localizacao_biblioteca) {
        this.Localizacao_biblioteca = Localizacao_biblioteca;
    }
    
}
