    public alunos montaraluno(){
        alunos a =  new alunos();
        a.setNome(txtfNome.getText());
        a.setCPF(txtfCPF.getText());
        a.setEndereco(txtfEndereco.getText());
        a.setTelefone(txtfTelefone.getText());
        a.setEmail(txtfEmail.getText());
        a.setDatadenascimento(txtfDatanascimento.getText());
        a.setSerie_Ano(txtfSerieAno.getText());
        a.setResponsavel(txtfNomedoresponsavel.getText());
        return a;
        
    }
